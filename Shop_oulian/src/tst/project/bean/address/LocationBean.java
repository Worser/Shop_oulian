package tst.project.bean.address;

public class LocationBean {
	private String lng;
	private String lat;
	
	public String getLng() {
		return lng;
	}
	public LocationBean setLng(String lng) {
		this.lng = lng;
		return this;
	}
	public String getLat() {
		return lat;
	}
	public LocationBean setLat(String lat) {
		this.lat = lat;
		return this;
	}
	
	
}
